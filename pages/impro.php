<h1>La troupe Improcité</h1>

<img src="images2/improcite-logo-med.png" style="float:left;width:130px;margin:20px;">

Improcité est une troupe de théâtre d'improvisation pour amateurs, et qui souhaite le rester !
<br/><br/>

Créé en 2002, c'est un collectif d'acteurs venant d'autres ligues qui ont voulu se regrouper pour former leur troupe autonome.
Certains d'entre eux se sont ensuite professionnalisés (<a href="http://www.schyzoz.fr/">Les Schyzoz</a>, <a href="http://labandeoriginale.jimdo.com/">La Bande originale</a>...)
<br/><br/>

<img src="images2/mascotte-transparent.png" style="float:right;width:130px;margin:20px;">

Passerelle entre les cours d'impro confirmés et l'impro pro ou semi-pro, notre souhait est de jouer régulièrement, à domicile ou à l'extérieur et d'explorer différentes formes d'improvisation.

Nos entrainements se déroulent avec des enseignants professionnels (<a href="http://www.impro-lyon.com/">La LILY</a>, <a href="http://www.improetcompagnie.com/">Et Compagine</a>...) que nous choisissons selon nos
envies et nos besoins d'improvisation.
<br/><br/>

Nous sommes une petite association et pour vivre et prospérer chacun met la main à la pâte, et prend des responsabilités.
Nous sommes fier des <a href="?p=comediens#apage">comédiens</a> de la troupe, qui amènent tant une cohésion de groupe et
une ambiance sympa qu'un un niveau d'impro où tout le monde se fait plaisir.
<br/><br/>

<h1>Les spectacles</h1>
<h2>Le Trokson</h2>

<img class="float_picture" src="images2/trokson-ext.jpg" style="float:right;width:130px;margin:20px;">

Situé au <a href="?p=lieux&id=4#amenu">110 Montéde la la Grande Côte</a> à Lyon,
<a href="http://www.trokson.com/">le Trokson</a> dispose d'une salle de concert séparée du bar,
et reçoit plusieurs fois par semaine des concerts de groupes de musique eclictiques, du punk au reggae.
<br/><br/>
Nous avons le plaisir de jouer dans ce lieu très Lyonnais les derniers mercredi de chaque mois.
<br/><br/>
Souvent organisé sous forme de catch 2 contre 2 (ou 4 contre l'arbitre :), les spectacles proposent chaque mois un thème différent.
<br/><br/>
La salle est assez petite (30-40 places, alors prensez à réserver !)


<h2>Le Ninkasi Gerland</h2>

<img class="float_picture" src="images2/ninka-gerland-ext.jpg" style="float:right;width:130px;margin:20px;">

Tous les deux mois environ, nous jouons au <a href="http://www.ninkasi.fr/">Ninkasi Gerland</a>, qui propose toute une actualité de <a href="http://www.ninkasi.fr/fr/agenda-complet.html">concerts</a> tout au long de l'année.
<br/><br/>
Ces représentations ont lieu le Dimanche après-midi, pour s'ouvrir aux personnes pour qui c'est compliqué le soir (oui les enfants...), et qui souhaitent découvrir l'improvisation sans se prendre la tête.
<br/><br/>
De plus ces représentations sont gratuites (a vot' bon coeur :)

<h2>Les matches à l'extérieur</h2>
Cinq ou six fois par an, nous nous déplacons à l'extérieur pour participer à des rencontres.
<br/><br/>
Montpellier, Bourgoin Jailleux, La Rochelle... quel plaisir de rencontrer de nouvelles troupes et de jouer avec eux pour le plus grand plaisir des spectateurs !

<h2>Les autres évènements</h2>
Improcité participe chaque année au <b>Téléthon</b> et organise un spectacle carritatif.
<br/><br/>
De plus, nous organisons depuis quelques années une rencontre entre 4 troupes de toute la france, spectacle baptisé <b>Imp'Rhone</b>.
