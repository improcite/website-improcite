<h1>Les spectacles</h1>
<h2>Sur Lyon</h2>
Nous donnons des spectacles sur Lyon une à deux fois par mois, dans différents lieux. Suivez notre <a href="?p=agenda#apage">agenda</a> pour pas manquer de date ! Nous jouons souvent à guichet fermé, pensez donc à réserver vos places si vous le pouvez.

<h2>Les matches à l'extérieur</h2>
Trois à quatre fois par an, nous nous déplacons à l'extérieur pour participer à des rencontres.

Montpellier, Bourgoin Jailleux, La Rochelle... quel plaisir de rencontrer de nouvelles troupes et de jouer avec eux pour le plus grand plaisir des spectateurs !

<h2>Les autres évènements</h2>
Improcité participe chaque année au <b>Téléthon</b> et organise un spectacle carritatif.

